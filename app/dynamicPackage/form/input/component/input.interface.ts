/**
 * Input component options
 */
export interface InputComponentOptions
{
    /**
     * Name of control in form
     */
    name: string;

    /**
     * Value of css class attribute for input element
     */
    inputCssClass: string;

    /**
     * Value of css class attribute for component itself
     */
    componentCssClass: string;

    /**
     * Value of css class attribute for validation div
     */
    validationDivCssClass: string;
}